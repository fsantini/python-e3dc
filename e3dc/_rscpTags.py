def getHexTag(tag_str):
    try:
        return rscpTags.keys()[rscpTags.values().index(tag_str)]
    except (KeyError):
        return int(tag_str, 0)

def getTag(tag_hex):
    try:
        return rscpTags[tag_hex]
    except (KeyError):
        return hex(tag_hex)

